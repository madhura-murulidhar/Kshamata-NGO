public class SurveyDetails {
    String dataOfSurvey;
    String phone;
    String emailId;
    String training;
    String skills;
    String levelOfSkill;
    String currentSalary;
    String currentLatitude;
    String currentLongitude;
    String job;
    String futurePlans;

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String getCurrentLatitude() {
        return currentLatitude;
    }

    public void setCurrentLatitude(String currentLatitude) {
        this.currentLatitude = currentLatitude;
    }

    public String getCurrentLongitude() {
        return currentLongitude;
    }

    public void setCurrentLongitude(String currentLongitude) {
        this.currentLongitude = currentLongitude;
    }


    public String getSkills() {
        return skills;
    }

    public void setSkills(String skills) {
        this.skills = skills;
    }

    public String getDataOfSurvey() {
        return dataOfSurvey;
    }

    public void setDataOfSurvey(String dataOfSurvey) {
        this.dataOfSurvey = dataOfSurvey;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    public String getTraining() {
        return training;
    }

    public void setTraining(String training) {
        this.training = training;
    }

    public String getLevelOfSkill() {
        return levelOfSkill;
    }

    public void setLevelOfSkill(String levelOfSkill) {
        this.levelOfSkill = levelOfSkill;
    }

    public String getCurrentSalary() {
        return currentSalary;
    }

    public void setCurrentSalary(String currentSalary) {
        this.currentSalary = currentSalary;
    }

    public String getFuturePlans() {
        return futurePlans;
    }

    public void setFuturePlans(String futurePlans) {
        this.futurePlans = futurePlans;
    }

}
